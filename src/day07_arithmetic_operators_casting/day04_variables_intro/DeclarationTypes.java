package day07_arithmetic_operators_casting.day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[]args){
        int mangoes=15;
        System.out.println(mangoes);
        int students= 400;
        System.out.println(students);
        students=401;
        System.out.println(students);

        int peopleInCity, birdsOnTree, fishInLake;
        peopleInCity=1000;
        birdsOnTree=15;
        fishInLake=40;

        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);

        int studentsinMO = 3, studentsinCA = 90;
        System.out.println(studentsinMO);
        System.out.println(studentsinCA);
    }
}
